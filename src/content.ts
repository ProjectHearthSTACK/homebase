// ─── Helpers ───────────────────────────────
export function getPillar(pillarId: string): Pillar | undefined {
  return pillars.find(p => p.id === pillarId)
}

export function getModule(moduleId: string): Module | undefined {
  for (const pillar of pillars) {
    const mod = pillar.modules.find(m => m.id === moduleId)
    if (mod) return mod
  }
  return undefined
}

export function getLesson(moduleId: string, lessonNumber: number): { lesson: Lesson; module: Module; pillar: Pillar } | undefined {
  for (const pillar of pillars) {
    const mod = pillar.modules.find(m => m.id === moduleId)
    if (mod) {
      const lesson = mod.lessons[lessonNumber - 1]
      if (lesson) return { lesson, module: mod, pillar }
    }
  }
  return undefined
}

export const totalModules = pillars.reduce((acc, p) => acc + p.modules.length, 0)
export const totalLessons = pillars.reduce((acc, p) => p.modules.reduce((a, m) => a + m.lessons.length, acc), 0)
