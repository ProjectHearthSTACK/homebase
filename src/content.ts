export interface Lesson {
  id: string
  title: string
  completed: boolean
}

export interface Module {
  id: string
  pillarId: string
  number: number
  title: string
  description: string
  emoji: string
  lessons: Lesson[]
  unlocked: boolean
  completed: boolean
}

export interface Pillar {
  id: string
  number: number
  title: string
  description: string
  emoji: string
  modules: Module[]
}

function makeLessons(pillarId: string, moduleNum: number, titles: string[]): Lesson[] {
  return titles.map((title, i) => ({
    id: `p${pillarId}-m${moduleNum}-l${i + 1}`,
    title,
    completed: false,
  }))
}

